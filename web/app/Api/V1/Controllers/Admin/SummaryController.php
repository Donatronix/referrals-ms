<?php

namespace App\Api\V1\Controllers\Admin;

use App\Api\V1\Controllers\Controller;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\DB;
use Throwable;

/**
 * Referral code Controller
 *
 * @package App\Api\V1\Controllers\Application
 */
class SummaryController extends Controller
{
    /**
     * Get referral and codes summary listing
     *
     * @OA\Get(
     *     path="/summary-listing",
     *     description="Get referral programm summary listing",
     *     tags={"Summary"},
     *
     *     security={{
     *         "default": {
     *             "ManagerRead",
     *             "User",
     *             "ManagerWrite"
     *         }
     *     }},
     *
     *     @OA\Response(
     *         response="200",
     *         description="TOP 1000 of leaders in the invitation referrals",
     *
     *         @OA\JsonContent(
     *             @OA\Property(
     *                 property="data",
     *                 type="object",
     *                 @OA\Property(
     *                     property="name",
     *                     type="string",
     *                     description="user name",
     *                     example=10,
     *                 ),
     *                 @OA\Property(
     *                     property="country",
     *                     type="string",
     *                     description="user country",
     *                     example=10,
     *                 ),
     *                 @OA\Property(
     *                     property="totalReferrals",
     *                     type="integer",
     *                     description="Total referrals by user",
     *                     example=10,
     *                 ),
     *                 @OA\Property(
     *                     property="totalCodesGenerated",
     *                     type="integer",
     *                     description="Total codes generated by user",
     *                     example=10,
     *                 ),
     *                 @OA\Property(
     *                     property="topReferralBonus",
     *                     type="integer",
     *                     description="Top referral bonus",
     *                     example=645000,
     *                 ),
     *                 @OA\Property(
     *                      property="amountEarned",
     *                      type="integer",
     *                      description="Amount earned by user",
     *                      example=450000,
     *                 ),
     *                 @OA\Property(
     *                      property="rank",
     *                      type="integer",
     *                      description="Rank of user",
     *                      example=450000,
     *                 ),
     *             ),
     *         ),
     *     ),
     *     @OA\Response(
     *         response="401",
     *         description="Unauthorized"
     *     ),
     *     @OA\Response(
     *         response=400,
     *         description="Invalid request"
     *     ),
     *     @OA\Response(
     *         response="404",
     *         description="User not found",
     *     ),
     *     @OA\Response(
     *         response="500",
     *         description="Unknown error"
     *     )
     * )
     *
     * @param Request $request
     *
     * @return mixed
     */
    public function listing(Request $request): mixed
    {
        try {
            $summary = DB::table('users')
                ->select(
                    'users.name',
                    'users.country',
                    DB::raw('COUNT(users.id) as totalReferrals'),
                    DB::raw('COUNT(referral_codes.id) as totalCodesGenerated'),
                    DB::raw('SUM(totals.reward) as topReferralBonus'),
                    DB::raw('SUM(totals.reward) as amountEarned'),
                    DB::raw('@rownum := @rownum + 1 AS rank')
                )
                ->join('referral_codes', 'users.referrer_id', '=', 'referral_codes.user_id')
                ->join('totals', 'users.referrer_id', '=', 'referral_codes.user_id')
                ->whereNotNull('referrer_id')->distinct('referrer_id')
                ->orderBy('rank', 'asc')
                ->groupBy('users.id')
                ->orderBy('totalCodesGenerated', 'desc')
                ->orderBy('totalReferrals', 'desc')
                ->orderBy('topReferralBonus', 'desc')
                ->orderBy('amountEarned', 'desc')
                ->paginate(request()->get('limit', config('settings.pagination_limit')));


            return response()->jsonApi([
                'type' => 'success',
                'title' => "List referral and codes summary",
                'message' => 'Referral and codes summary successfully received',
                'data' => $summary,
            ], 200);
        } catch (Throwable $e) {
            return response()->jsonApi([
                'type' => 'danger',
                'title' => "Not received list",
                'message' => $e->getMessage(),
            ], 404);
        }
    }
}
